import streamlit as st

def render_student_rights():
    st.title("Student Rights & Responsibilities")
    st.markdown("""
    ### Student Rights
    - **Right to Appeal**: Students can appeal decisions related to academic misconduct.
    - **Right to Representation**: Students may have a representative during hearings.
    - **Right to Privacy**: Personal information must be protected.

    ### Responsibilities
    - **Honesty**: Submit original work and avoid dishonest practices.
    - **Compliance**: Follow institutional policies and procedures.

    For more information, refer to your institution's student handbook.
    """)